            <h2 id="cosa-rimane">Cosa rimane nel corpo dopo un trauma</h2>
            <p>
              Il corpo umano è straordinariamente adattabile. Quando subisce un trauma, risponde
              immediatamente con meccanismi di protezione: spasmi muscolari, inibizione di certi
              movimenti, redistribuzione del carico. Questi meccanismi sono vitali nell'acuto,
              ma se non vengono "disattivati" una volta superata la fase critica, diventano
              <strong>pattern disfunzionali cronici</strong>.
            </p>
            <p>
              Ecco perché molte persone, mesi o anni dopo un infortunio, continuano ad avere
              dolori in zone apparentemente distanti dall'area originariamente traumatizzata:
              la caviglia che va in distorsione due volte l'anno, la schiena che fa male ogni
              volta che si pratica sport, la spalla che "non è più la stessa" dopo una lussazione.
              L'osteopatia identifica e tratta queste catene disfunzionali.
            </p>

