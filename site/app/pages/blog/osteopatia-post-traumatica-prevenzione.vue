            <h2 id="colpo-di-frusta">Il colpo di frusta: perché sottovalutarlo è un errore</h2>
            <p>
              Il <strong>colpo di frusta</strong> (whiplash) è forse il trauma più frequente
              sulle strade italiane e uno dei più sottovalutati. L'impatto improvviso della testa
              durante un tamponamento o uno scontro frontale causa una iper-estensione e iper-flessione
              rapidissima del rachide cervicale, con effetti che si propagano ben oltre il collo:
              colonna dorsale e lombare, diaframma, sistema nervoso autonomo.
            </p>
            <p>
              Molte persone riferiscono di sentirsi "bene" nei giorni immediatamente successivi
              all'incidente, per poi sviluppare nelle settimane o mesi successivi cefalee,
              vertigini, cervicalgia cronica, disturbi del sonno e calo della concentrazione.
              Questo ritardo è tipico del whiplash e dipende dall'attivazione progressiva delle
              tensioni fasciali profonde.
            </p>
            <p>
              Il trattamento osteopatico del colpo di frusta è efficace e va iniziato
              <strong>non appena l'acuto è risolto</strong> (solitamente dopo 10–15 giorni
              dall'evento), sempre dopo aver escluso fratture o lesioni strutturali con gli
              esami diagnostici appropriati.
            </p>

