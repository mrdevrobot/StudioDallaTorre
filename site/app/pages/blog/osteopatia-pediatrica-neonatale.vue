              genitori allo studio. Il bambino piange in modo inconsolabile, soprattutto nelle ore
              serali, per settimane. La causa non è sempre di origine gastrointestinale: spesso
              c'è una componente di tensione diaframmatica o cervicale che impedisce una corretta
              peristalsi e aumenta il disagio viscerale.
            </p>
