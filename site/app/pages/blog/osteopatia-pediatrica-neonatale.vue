              L'approccio osteopatico non è invasivo: il trattamento del neonato avviene attraverso
              <strong>contatti leggeri come una carezza</strong>, rispettando la delicatezza del
              piccolo corpo. Le tecniche cranioSacrali e viscerali utilizzate in età neonatale
              sono completamente diverse da quelle degli adulti e non prevedono manipolazioni
              articolari.
            </p>
